package cz.payas.model;

public class Vertex {

	private Float x;
	private Float y;
	private Float z;
	
	public Vertex() {
	}

	public Vertex(Float x1, Float y1, Float z1) {
		super();
		this.x = x1;
		this.y = y1;
		this.z = z1;
	}

	public Float getX() {
		return x;
	}

	public void setX(Float x) {
		this.x = x;
	}

	public Float getY() {
		return y;
	}

	public void setY(Float y) {
		this.y = y;
	}

	public Float getZ() {
		return z;
	}

	public void setZ(Float z) {
		this.z = z;
	}

	
	
}
